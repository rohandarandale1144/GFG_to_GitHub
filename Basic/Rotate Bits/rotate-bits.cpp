#include<bits/stdc++.h>
using namespace std;

// } Driver Code Ends
class Solution
{
  public:
        vector <int> rotate (int n, int d)
        {
            d=d%16;
            int a=n,b=n,c=n,e=n;
            unsigned short left=(a<<d)| (b>>(16-d));
            unsigned short right=(c>>d) | (e<<(16-d));
            
            return {left,right};
        }
};

//{ Driver Code Starts.
int main()
{
    int t; cin >> t;
    while (t--)
    {
        
        int n, d; cin >> n >> d;
        Solution ob;
        vector <int> res = ob.rotate (n, d);
        cout << res[0] << endl << res[1] << endl;
    }
}
