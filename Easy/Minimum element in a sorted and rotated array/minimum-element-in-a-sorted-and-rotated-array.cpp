class Solution{
public:
    int findMin(int arr[], int n){
        //complete the function here
        int min=arr[0];
        for(int i=0;i<n;i++)
        {
            if(min>arr[i])
            {
                min=arr[i];
            }
        }
        return min;
    }
};

